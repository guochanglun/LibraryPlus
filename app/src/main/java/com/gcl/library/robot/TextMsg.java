package com.gcl.library.robot;

/**
 * 文本类消息
 * Created by gcl on 2017/3/7.
 */
public class TextMsg {

    private int code;
    private String text;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
